def nlerp(qa, qb, t):
    ax, ay, az, aw = qa
    bx, by, bz, bw = qb
    rx = ax * (1 - t) + bx * t
    ry = ay * (1 - t) + by * t
    rz = az * (1 - t) + bz * t
    rw = aw * (1 - t) + bw * t
    n = math.sqrt(rx * rx + ry * ry + rz * rz + rw * rw)
    if n < 1e-8:
        return list(qb)
    return [rx / n, ry / n, rz / n, rw / n]


def main():
    t0 = ANCHORS[0][0]
    t1 = ANCHORS[-1][0]
    samples = []
    steps_per_seg = 14
    for i in range(len(ANCHORS) - 1):
        ta, xa, ya, za, qax, qay, qaz, qaw = ANCHORS[i]
        tb, xb, yb, zb, qbx, qby, qbz, qbw = ANCHORS[i + 1]
        for k in range(steps_per_seg):
            u = k / steps_per_seg
            tt = ta + (tb - ta) * u
            px = xa + (xb - xa) * u
            py = ya + (yb - ya) * u
            pz = za + (zb - za) * u
            q = nlerp((qax, qay, qaz, qaw), (qbx, qby, qbz, qbw), u)
            samples.append(
                {
                    "t": tt - t0,
                    "px": px,
                    "py": py,
                    "pz": pz,
                    "qx": q[0],
                    "qy": q[1],
                    "qz": q[2],
                    "qw": q[3],
                }
            )

    ta, xa, ya, za, qax, qay, qaz, qaw = ANCHORS[-1]
    samples.append(
        {
            "t": t1 - t0,
            "px": xa,
            "py": ya,
            "pz": za,
            "qx": qax,
            "qy": qay,
            "qz": qaz,
            "qw": qaw,
        }
    )

    payload = {
        "format": "openfeed-scene-camera-path-v1",
        "sceneName": "GroceryStore",
        "recordedDurationSeconds": t1 - t0,
        "sampleIntervalSeconds": (t1 - t0) / max(1, len(samples) - 1),
        "samples": samples,
    }

    os.makedirs(os.path.dirname(OUT), exist_ok=True)
    with open(OUT, "w", encoding="utf-8") as f:
        json.dump(payload, f, separators=(",", ":"))
    print("Wrote", OUT, "samples=", len(samples))


if __name__ == "__main__":
    main()
